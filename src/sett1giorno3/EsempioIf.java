package sett1giorno3;

public class EsempioIf {
    public static void main(String[] args) {
        // First usage of IF construct
        int difficoltaJava = 7;

        if (difficoltaJava > 5) {
            System.out.println("Java è difficile");
        } else {
            System.out.println("Java è facile");
        }
        System.out.println("Fine programma");
    }
}
